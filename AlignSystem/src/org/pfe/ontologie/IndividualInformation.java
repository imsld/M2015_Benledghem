package org.pfe.ontologie;

public class IndividualInformation {
	private String individu;
	private String type;

	public IndividualInformation(String individu, String type) {
		super();
		this.individu = individu;
		this.type = type;
	}

	public String getIndividu() {
		return individu;
	}

	public String getType() {
		return type;
	}
}
